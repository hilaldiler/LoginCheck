    private void j_tableDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_j_tableDisplayMouseClicked
        // TODO add your handling code here:
        int i = j_tableDisplay.getSelectedRow();
        
        TableModel model = j_tableDisplay.getModel();
        name.setText(model.getValueAt(i, 1).toString());
        String gender1 = model.getValueAt(i, 2).toString();
        if(gender1.equalsIgnoreCase("female"))
        {
            female.setSelected(true);
        }
        else
        {
            male.setSelected(true);
        }
        
        address.setText(model.getValueAt(i, 3).toString());
        
        String department1 = model.getValueAt(i, 4).toString();
        switch(department1) {
            case "Bilgisayar Mühendisliği":
                department.setSelectedIndex(0);
                break;
            case "Endüstri Mühendisliği":
                department.setSelectedIndex(1);
                break;
            case "Yönetim Bilisim Sistemleri":
                department.setSelectedIndex(2);
                break;
            case "Elektrik-Elektronik Mühendisliği":
                department.setSelectedIndex(3);
            case "İşletme Enformatiği":
                department.setSelectedIndex(4);
                break;
            
            
        }
        
        String course = model.getValueAt(i, 5).toString();
        switch(course) {
            case "JAVA":
                java.setSelected(true);
                c.setSelected(false);
                pyhton.setSelected(false);
                break;
            case "C":
                c.setSelected(true);
                java.setSelected(false);
                pyhton.setSelected(false);
                break;
            default:
                c.setSelected(true);
                java.setSelected(true);
                pyhton.setSelected(true);
                
        }
        
        
    }//GEN-LAST:event_j_tableDisplayMouseClicked

