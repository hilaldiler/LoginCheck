    public ArrayList <User> userList() {
        ArrayList<User> userList = new ArrayList<>();
        
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName = dbtest;IntegratedSecurity=true";
            Connection con = DriverManager.getConnection(url);
            String query = "SELECT*FROM users";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            User user ;
            while(rs.next()) {
                user = new User(rs.getInt("uid"), rs.getString("name"), rs.getString("gender"), 
                        rs.getString("address"), rs.getString("department"), rs.getString("course"));
                userList.add(user);
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return userList;
    }
    public void showUser() {
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)j_tableDisplay.getModel();
        Object[] row = new Object[6];
        
        for(int i = 0;i < list.size(); i++) {
        row[0] = list.get(i).getuid();
        row[1] = list.get(i).getname();
        row[2] = list.get(i).getgender();
        row[3] = list.get(i).getaddress();
        row[4] = list.get(i).getdepartment();
        row[5] = list.get(i).getcourse();

        model.addRow(row);

                }
    }

