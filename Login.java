    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        //sql server connection string
        
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                 String url = "jdbc:sqlserver://localhost:1433;databaseName=dbtest;integratedSecurity=true";
               
                 Connection con = DriverManager.getConnection(url);
                 String sql = "SELECT * FROM testdb WHERE username = ? AND password = ?";  
                 
                 PreparedStatement pst = con.prepareStatement(sql);
                 pst.setString(1, username.getText());
                 pst.setString(2, password.getText());
                 
                 ResultSet rs = pst.executeQuery(); //Sorgunun sonucunu dondurur
                 
                 if(rs.next()){
                     JOptionPane.showMessageDialog(null, "Username and password is match");
                     studentCheckin checkin = new studentCheckin();
                     checkin.setVisible(true);
                     setVisible(false);
                 }else {
                     JOptionPane.showMessageDialog(null, "Username and password is not match");
                     username.setText("");
                     password.setText("");

                 }
                 con.close();
        }catch(Exception e){
           
            JOptionPane.showMessageDialog(null, e);
        }
       
    }//GEN-LAST:event_signInActionPerformed

